public class Location {
    public double latitude;
    public double longitude;
    public String name;
    public int id;

    public double getLatitude() {
        return latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }
}
