public class Location
{
    private double latitude ;
    private double longitude ;
    private String name ;
    private int id ;

    public double getLatitude() {
        return latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }
}